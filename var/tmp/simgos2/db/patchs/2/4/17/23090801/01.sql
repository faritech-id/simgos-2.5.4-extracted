DROP PROCEDURE IF EXISTS `getTarifAdministrasi`;
DELIMITER //
CREATE PROCEDURE `getTarifAdministrasi`(
	IN `PJENIS` SMALLINT,
	IN `PJENIS_KUNJUNGAN` SMALLINT,
	IN `PTANGGAL` DATETIME,
	OUT `PTARIF_ID` INT,
	OUT `PTARIF` INT
)
BEGIN
	SELECT ID, TARIF INTO PTARIF_ID, PTARIF 
	  FROM `master`.tarif_administrasi tp
	 WHERE tp.ADMINISTRASI = PJENIS
	   AND tp.JENIS_KUNJUNGAN = PJENIS_KUNJUNGAN
	   AND tp.TANGGAL_SK <= PTANGGAL
	 ORDER BY tp.TANGGAL DESC LIMIT 1;
	 
	IF PTARIF_ID IS NULL THEN
		SELECT ID, TARIF INTO PTARIF_ID, PTARIF 
		  FROM `master`.tarif_administrasi tp
		 WHERE tp.ADMINISTRASI = PJENIS
		   AND tp.JENIS_KUNJUNGAN = PJENIS_KUNJUNGAN
		   AND tp.STATUS = 1
	 	 ORDER BY tp.TANGGAL DESC LIMIT 1;
	END IF;
	 
	IF PTARIF_ID IS NULL THEN
		SET PTARIF_ID = NULL;
		SET PTARIF = 0;
	END IF;
END//
DELIMITER ;

