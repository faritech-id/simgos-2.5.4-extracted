CREATE PROCEDURE `storeO2`(
	IN `PKUNJUNGAN` CHAR(19)
)
BEGIN
	DECLARE VNOPEN, VRUANGAN CHAR(10);
	DECLARE VTAGIHAN, VTAGIHAN_TERPISAH CHAR(10);
	DECLARE VJENIS_KUNJUNGAN TINYINT;
