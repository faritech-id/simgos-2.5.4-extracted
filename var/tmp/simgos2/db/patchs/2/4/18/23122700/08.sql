USE `berkas_klaim`;

CREATE TABLE IF NOT EXISTS `patologi_anatomi_detil` (
  `ID` int NOT NULL AUTO_INCREMENT,
  `PATOLOGI_ANATOMI` int NOT NULL,
  `HASIL_PA` int NOT NULL,
  `STATUS` tinyint NOT NULL DEFAULT '1',
  PRIMARY KEY (`ID`) USING BTREE,
  KEY `STATUS` (`STATUS`) USING BTREE,
  KEY `HASIL_PA` (`HASIL_PA`) USING BTREE,
  KEY `PATOLOGI_ANATOMI` (`PATOLOGI_ANATOMI`) USING BTREE
) ENGINE=InnoDB;