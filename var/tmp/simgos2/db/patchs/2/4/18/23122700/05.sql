USE `berkas_klaim`;

CREATE TABLE IF NOT EXISTS `obat_farmasi` (
  `ID` int NOT NULL AUTO_INCREMENT,
  `PENDAFTARAN` char(10) NOT NULL,
  `NOSEP` varchar(25) NOT NULL,
  `TANGGAL` timestamp NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `OLEH` smallint NOT NULL,
  `STATUS` tinyint NOT NULL DEFAULT '1',
  PRIMARY KEY (`ID`) USING BTREE,
  UNIQUE KEY `PENDAFTARAN_NOSEP` (`PENDAFTARAN`,`NOSEP`) USING BTREE,
  KEY `PENDAFTARAN` (`PENDAFTARAN`) USING BTREE,
  KEY `STATUS` (`STATUS`) USING BTREE,
  KEY `NOSEP` (`NOSEP`)
) ENGINE=InnoDB;
