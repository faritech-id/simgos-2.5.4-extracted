-- Membuang struktur basisdata untuk informasi

USE `informasi`;

-- membuang struktur untuk procedure informasi.executePendapatan
DROP PROCEDURE IF EXISTS `executePendapatan`;
DELIMITER //
CREATE PROCEDURE `executePendapatan`(
	IN `PTGL_AWAL` DATE,
	IN `PTGL_AKHIR` DATE
)
BEGIN
	DECLARE VTGL_AWAL DATETIME;
	DECLARE VTGL_AKHIR DATETIME;
		
	SET VTGL_AWAL = CONCAT(PTGL_AWAL, ' 00:00:01');
	SET VTGL_AKHIR = CONCAT(PTGL_AKHIR, ' 23:59:59');
	
	BEGIN		
		DECLARE VTANGGAL DATE;
		DECLARE VID VARCHAR(50);
		DECLARE VDESKRIPSI VARCHAR(200);
		DECLARE VCARABAYAR VARCHAR(200);
		DECLARE VIDCARABAYAR VARCHAR(5);
		DECLARE VVALUE BIGINT;
		DECLARE VLASTUPDATED DATETIME;
		
		DECLARE EXEC_NOT_FOUND TINYINT DEFAULT FALSE;		
		DECLARE CR_EXEC CURSOR FOR SELECT DATE(tg.TANGGAL) TANGGAL
						, 0 ID, '' DESKRIPSI, ref.DESKRIPSI CARABAYAR, ref.ID IDCARABAYAR
						, SUM((SELECT (t.TOTAL) FROM pembayaran.tagihan t WHERE t.ID=tgp.TAGIHAN AND t.`STATUS`=2)) VALUE, MAX(tg.TANGGAL) LASTUPDATED
					FROM pembayaran.pembayaran_tagihan tg
						, pembayaran.tagihan_pendaftaran tgp
						, pendaftaran.pendaftaran pd
						  LEFT JOIN pendaftaran.penjamin pj ON pd.NOMOR=pj.NOPEN
						  LEFT JOIN master.referensi ref ON pj.JENIS=ref.ID AND ref.JENIS=10 AND ref.`STATUS`!=0
					WHERE tg.TAGIHAN=tgp.TAGIHAN AND tg.`STATUS`!=0 AND tgp.`STATUS`!=0 AND tg.JENIS=1 AND tgp.UTAMA=1
					  AND tgp.PENDAFTARAN=pd.NOMOR AND pd.`STATUS`!=0
					  AND tg.TANGGAL BETWEEN VTGL_AWAL AND VTGL_AKHIR AND pj.JENIS IS NOT NULL AND pj.JENIS!=0 AND ref.ID IS NOT NULL
					GROUP BY DATE(tg.TANGGAL), pj.JENIS;
			
		DECLARE CONTINUE HANDLER FOR NOT FOUND SET EXEC_NOT_FOUND = TRUE;
						
		OPEN CR_EXEC;
						
		EXIT_EXEC: LOOP
			FETCH CR_EXEC INTO VTANGGAL, VID, VDESKRIPSI, VCARABAYAR, VIDCARABAYAR, VVALUE, VLASTUPDATED;								
							
			IF EXEC_NOT_FOUND THEN
				LEAVE EXIT_EXEC;
			END IF;
			
			IF EXISTS(SELECT 1 FROM informasi.pendapatan WHERE TANGGAL = VTANGGAL AND ID = VID AND IDCARABAYAR = VIDCARABAYAR) THEN
				UPDATE informasi.pendapatan
				   SET VALUE = VVALUE, LASTUPDATED = VLASTUPDATED
				  WHERE TANGGAL = VTANGGAL AND ID = VID AND IDCARABAYAR = VIDCARABAYAR;
			ELSE
				INSERT INTO informasi.pendapatan(TANGGAL, ID, DESKRIPSI, CARABAYAR, IDCARABAYAR, VALUE, LASTUPDATED)
					  VALUES (VTANGGAL, VID, VDESKRIPSI, VCARABAYAR, VIDCARABAYAR, VVALUE, VLASTUPDATED);
			END IF;
		END LOOP;
		
		CLOSE CR_EXEC;
	END;		
END//
DELIMITER ;

