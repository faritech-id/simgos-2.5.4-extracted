USE `informasi`;

-- membuang struktur untuk procedure informasi.listKlaimInacbg
DROP PROCEDURE IF EXISTS `listKlaimInacbg`;
DELIMITER //
CREATE PROCEDURE `listKlaimInacbg`(IN `PTGL_AWAL` DATE, IN `PTGL_AKHIR` DATE, IN `PTYPE` TINYINT)
BEGIN	
	IF PTYPE < 9 THEN
		SET @sqlText = CONCAT('
			SELECT YEAR(TANGGAL) TAHUN, MONTH(TANGGAL) IDBULAN, b.NAMA BULAN, p.ID, DESKRIPSI, CARABAYAR, IDCARABAYAR, SUM(VALUE) VALUE, MAX(LASTUPDATED) LASTUPDATED
			  FROM informasi.klaim_inacbg p LEFT JOIN master.bulan b ON MONTH(TANGGAL) = b.ID
			 WHERE TANGGAL BETWEEN ''', PTGL_AWAL, ''' AND ''', PTGL_AKHIR, '''',
			 IF(PTYPE = 2, 'GROUP BY MONTH(TANGGAL), p.ID, IDCARABAYAR ',
			 	IF(PTYPE = 3, 'GROUP BY YEAR(TANGGAL), p.ID, IDCARABAYAR ',
			 		IF(PTYPE = 4, 'GROUP BY YEAR(TANGGAL), MONTH(TANGGAL), p.ID ',
			 			IF(PTYPE = 5, 'GROUP BY YEAR(TANGGAL), p.ID', 'GROUP BY p.ID, IDCARABAYAR ')
			 		)
			 	)
			 ),'
			 ORDER BY TAHUN, IDBULAN, ID, IDCARABAYAR');
		PREPARE stmt FROM @sqlText;
		EXECUTE stmt;
		DEALLOCATE PREPARE stmt;
	ELSE
	BEGIN 
		DECLARE VTGL_AWAL_BLN DATE;
		DECLARE VTGL_AWAL_THN DATE;
		
		SET VTGL_AWAL_BLN = DATE_FORMAT(PTGL_AWAL, '%Y-%m-01');
		SET VTGL_AWAL_THN = DATE_FORMAT(PTGL_AWAL, '%Y-01-01');
		
		SELECT * FROM (
			SELECT 1 ID, SUM(VALUE) VALUE, MAX(LASTUPDATED) LASTUPDATED
			  FROM informasi.klaim_inacbg
			 WHERE TANGGAL BETWEEN PTGL_AWAL AND PTGL_AKHIR
			UNION 
			SELECT 2 ID, SUM(VALUE) VALUE, MAX(LASTUPDATED) LASTUPDATED
			  FROM informasi.klaim_inacbg
			 WHERE TANGGAL BETWEEN VTGL_AWAL_BLN AND PTGL_AKHIR
			UNION 
			SELECT 3 ID, SUM(VALUE) VALUE, MAX(LASTUPDATED) LASTUPDATED
			  FROM informasi.klaim_inacbg
			WHERE TANGGAL BETWEEN VTGL_AWAL_THN AND PTGL_AKHIR 
		) A;
	END;
	END IF;
END//
DELIMITER ;

