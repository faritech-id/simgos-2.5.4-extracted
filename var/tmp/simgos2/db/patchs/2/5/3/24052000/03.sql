-- membuang struktur untuk procedure aplikasi.doAutomaticExecute02
DROP PROCEDURE IF EXISTS `doAutomaticExecute02`;
DELIMITER //
CREATE PROCEDURE `doAutomaticExecute02`(
	IN `PIS_PROSEDUR` TINYINT,
	IN `PLIMIT` SMALLINT
)
BEGIN
	DECLARE VID INT;
	DECLARE VPERINTAH TEXT;
	DECLARE VIDS TEXT DEFAULT '';
	DECLARE VPERINTAHS TEXT DEFAULT '';
	DECLARE VFOUND TINYINT DEFAULT FALSE;
	DECLARE DATA_NOT_FOUND INT DEFAULT FALSE;
	
	DECLARE CRDATA CURSOR FOR
		SELECT ID, PERINTAH 
		  FROM aplikasi.automaticexecute_02
		 WHERE STATUS = 0
		   AND IS_PROSEDUR = PIS_PROSEDUR
		 ORDER BY ID LIMIT PLIMIT;
	
	DECLARE CONTINUE HANDLER FOR NOT FOUND SET DATA_NOT_FOUND = TRUE;
		
	OPEN CRDATA;
		DATA_END: LOOP
			FETCH CRDATA INTO VID, VPERINTAH;
		
			IF DATA_NOT_FOUND THEN
				UPDATE temp.temp SET ID = 0 WHERE ID = 0;
				LEAVE DATA_END;
			END IF;	
			
			IF NOT VFOUND THEN
				SET VFOUND = TRUE;
			END IF;
			
			UPDATE aplikasi.automaticexecute_02 SET STATUS = 1 WHERE ID = VID;
			
			SET VIDS = CONCAT(VIDS, ',', VID);
			
			SET @PERINTAH = VPERINTAH;
			PREPARE stmt FROM @PERINTAH;
			EXECUTE stmt;
			DEALLOCATE PREPARE stmt;
		END LOOP;
	CLOSE CRDATA;
	
	IF VFOUND THEN
		SET @DOPERINTAH = CONCAT('DELETE FROM aplikasi.automaticexecute_02 WHERE `STATUS` = 1');
		PREPARE stmt FROM @DOPERINTAH;
		EXECUTE stmt;
		DEALLOCATE PREPARE stmt;
	
	END IF;
END//
DELIMITER ;

