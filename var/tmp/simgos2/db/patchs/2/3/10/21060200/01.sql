USE pembayaran;

CREATE TABLE IF NOT EXISTS `pelunasan_piutang_pasien` (
  `ID` int NOT NULL AUTO_INCREMENT,
  `TAGIHAN_PIUTANG` char(10) DEFAULT NULL,
  `ANGSURAN_KE` int NOT NULL,
  `JUMLAH_ANGSURAN_DIBAYAR` int NOT NULL COMMENT 'JUMLAH ANGSURAN YANG DI BAYAR',
  `TOTAL_BAYAR` decimal(10,2) NOT NULL,
  `JENIS_PEMBAYARAN` tinyint NOT NULL,
  `TANGGAL` timestamp NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `OLEH` int NOT NULL,
  `STATUS` int NOT NULL DEFAULT '1',
  PRIMARY KEY (`ID`),
  KEY `STATUS` (`STATUS`),
  KEY `TAGIHAN_PIUTANG` (`TAGIHAN_PIUTANG`),
  KEY `JENIS_PEMBAYARAN` (`JENIS_PEMBAYARAN`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;
