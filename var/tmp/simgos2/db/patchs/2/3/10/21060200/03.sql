USE pembayaran;

CREATE TABLE IF NOT EXISTS `transfer` (
  `ID` int NOT NULL AUTO_INCREMENT,
  `TAGIHAN` char(10) NOT NULL,
  `BANK_ASAL` tinyint NOT NULL,
  `REKENING` int NOT NULL DEFAULT '0',
  `NAMA_PENYETOR` varchar(150) NOT NULL DEFAULT '',
  `REF` varchar(100) CHARACTER SET latin1 COLLATE latin1_swedish_ci NOT NULL DEFAULT '0' COMMENT 'Menyimpan ref transfer',
  `TANGGAL_TRANSFER` datetime NOT NULL,
  `TOTAL` decimal(60,2) NOT NULL DEFAULT '0.00',
  `TANGGAL` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `OLEH` int NOT NULL DEFAULT '0',
  `JENIS` INT(10) NOT NULL DEFAULT '0' COMMENT '1. SETOR KE BANG, 2. TRANSFER',
  `STATUS` int NOT NULL DEFAULT '1',
  PRIMARY KEY (`ID`),
  KEY `STATUS` (`STATUS`),
  KEY `TAGIHAN` (`TAGIHAN`),
  KEY `BANK_ASAL` (`BANK_ASAL`),
  KEY `BANK_TUJUAN` (`REKENING`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=latin1;