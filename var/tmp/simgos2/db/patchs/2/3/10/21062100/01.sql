USE layanan;
CREATE TABLE `nilai_kritis_lab` (
	`ID` INT(10) NOT NULL AUTO_INCREMENT,
	`HASIL_LAB` INT(10) NOT NULL COMMENT 'ID Hasil Lab',
	`PELAPOR` VARCHAR(100) NULL DEFAULT NULL COMMENT 'Dokter/Petugas lab yang melakukan konfirmasi',
	`PENERIMA` VARCHAR(100) NULL DEFAULT NULL COMMENT 'Perawat/Dokter yang menerima konfirmasi',
	`STATUS_LAPOR` TINYINT(3) NOT NULL DEFAULT '0',
	`WAKTU_LAPOR` DATETIME NOT NULL,
	`TANGGAL` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
	`OLEH` SMALLINT(5) NULL DEFAULT NULL,
	`STATUS` TINYINT(3) NOT NULL DEFAULT '1',
	PRIMARY KEY (`ID`) USING BTREE,
	INDEX `HASIL_LAB` (`HASIL_LAB`) USING BTREE
)
ENGINE=InnoDB
;