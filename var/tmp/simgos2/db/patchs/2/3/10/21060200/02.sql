USE pembayaran;

CREATE TABLE IF NOT EXISTS `pelunasan_piutang_perusahaan` (
  `ID` int NOT NULL AUTO_INCREMENT,
  `TAGIHAN_PIUTANG` char(10) CHARACTER SET latin1 COLLATE latin1_swedish_ci NOT NULL DEFAULT '',
  `PENJAMIN` tinyint NOT NULL DEFAULT '0',
  `TOTAL_PIUTANG` decimal(60,2) NOT NULL DEFAULT '0.00',
  `TOTAL_BAYAR` decimal(60,2) NOT NULL DEFAULT '0.00',
  `TANGGAL` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `OLEH` int NOT NULL DEFAULT '0',
  `STATUS` int NOT NULL DEFAULT '1',
  PRIMARY KEY (`ID`),
  KEY `STATUS` (`STATUS`),
  KEY `TAGIHAN_PENJAMIN` (`TAGIHAN_PIUTANG`,`PENJAMIN`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=latin1;