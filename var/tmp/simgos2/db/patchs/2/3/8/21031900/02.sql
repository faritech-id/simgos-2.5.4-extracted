CREATE DATABASE `penjamin_rs`;

USE `penjamin_rs`;

CREATE TABLE `dpjp` (
	`ID` INT NOT NULL AUTO_INCREMENT,
	`PENJAMIN` INT NOT NULL,
	`DPJP_PENJAMIN` CHAR(15) NOT NULL DEFAULT '',
	`DPJP_RS` SMALLINT NOT NULL DEFAULT 0,
	`TANGGAL` DATETIME NOT NULL,
	`STATUS` TINYINT(1) NOT NULL DEFAULT 1,
	PRIMARY KEY (`ID`, `PENJAMIN`)
);
