CREATE DEFINER=`root`@`127.0.0.1` FUNCTION `getStokAwal`(`PBARANG_RUANGAN` BIGINT, `PTANGGAL` DATETIME) RETURNS decimal(60,2)
    DETERMINISTIC
BEGIN
	DECLARE VSTOK DECIMAL(60, 2);
	DECLARE VJENIS TINYINT;
	DECLARE VJUMLAH DECIMAL(60, 2);
	DECLARE VTAMBAH_ATAU_KURANG CHAR(1);
	
	SELECT JENIS, JUMLAH, STOK INTO VJENIS, VJUMLAH, VSTOK
	  FROM inventory.transaksi_stok_ruangan
	 WHERE TANGGAL > PTANGGAL
		AND BARANG_RUANGAN = PBARANG_RUANGAN
	 ORDER BY TANGGAL ASC LIMIT 1;
	 
	 
	SELECT jts.TAMBAH_ATAU_KURANG INTO VTAMBAH_ATAU_KURANG
	  FROM inventory.jenis_transaksi_stok jts
	 WHERE jts.ID = VJENIS;
	 
	IF FOUND_ROWS() > 0 THEN
		IF VTAMBAH_ATAU_KURANG = '+' THEN
			SET VSTOK = VSTOK - VJUMLAH;
		ELSEIF VTAMBAH_ATAU_KURANG = '-' THEN
			SET VSTOK = VSTOK + VJUMLAH;
		END IF;
	END IF;
 	
	RETURN IF(FOUND_ROWS() > 0, VSTOK, 0);
END//
