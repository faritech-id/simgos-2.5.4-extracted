-- membuang struktur untuk trigger pendaftaran.onBeforeInsertKunjungan
DROP TRIGGER IF EXISTS `onBeforeInsertKunjungan`;
SET @OLDTMP_SQL_MODE=@@SQL_MODE, SQL_MODE='NO_ENGINE_SUBSTITUTION';
DELIMITER //
CREATE TRIGGER `onBeforeInsertKunjungan` BEFORE INSERT ON `kunjungan` FOR EACH ROW BEGIN
	DECLARE VNORM INT;
	DECLARE VRESERVASI CHAR(10);
	DECLARE VRUANGKAMARTIDUR SMALLINT;
	DECLARE VIKUT_IBU TINYINT;
	DECLARE VKUNJUNGAN_IBU CHAR(19);
	DECLARE VID VARCHAR(2);
		
	SET VID = LEFT(NEW.REF, 2);
		
	SELECT NORM INTO VNORM
	  FROM pendaftaran.pendaftaran
	 WHERE NOMOR = NEW.NOPEN;
	
	IF FOUND_ROWS() > 0 THEN
		SELECT NORM INTO VNORM 
		  FROM pendaftaran.pendaftaran a, pendaftaran.kunjungan b
		 WHERE a.NORM =  VNORM
		   AND a.NOMOR = b.NOPEN
		   AND b.RUANGAN = NEW.RUANGAN
		   AND b.`STATUS` = 2
		 ORDER BY b.MASUK LIMIT 1;
		 
		IF FOUND_ROWS() = 0 THEN
			SET NEW.BARU = 1;
		ELSE
			SET NEW.BARU = 0;
		END IF;
	END IF;
	
	IF NEW.REF IS NULL OR NEW.REF = '' THEN
		SELECT RESERVASI, IKUT_IBU, KUNJUNGAN_IBU INTO VRESERVASI, VIKUT_IBU, VKUNJUNGAN_IBU FROM pendaftaran.tujuan_pasien WHERE NOPEN = NEW.NOPEN;			
	ELSE
		IF VID = '11' THEN
			SELECT RESERVASI, IKUT_IBU, KUNJUNGAN_IBU INTO VRESERVASI, VIKUT_IBU, VKUNJUNGAN_IBU FROM pendaftaran.mutasi WHERE NOMOR = NEW.REF;
		END IF;
	END IF;
	
	IF FOUND_ROWS() > 0 THEN
		IF VIKUT_IBU = 1 THEN
			SELECT k.RUANG_KAMAR_TIDUR INTO VRUANGKAMARTIDUR
			  FROM pendaftaran.kunjungan k 
			 WHERE k.NOMOR = VKUNJUNGAN_IBU;
			 
			IF FOUND_ROWS() > 0 THEN
				SET NEW.RUANG_KAMAR_TIDUR = VRUANGKAMARTIDUR;
			END IF;
		ELSE
			IF NOT VRESERVASI IS NULL OR VRESERVASI != '' THEN
				SELECT RUANG_KAMAR_TIDUR INTO VRUANGKAMARTIDUR FROM pendaftaran.reservasi WHERE NOMOR = VRESERVASI;
				
				IF FOUND_ROWS() > 0 THEN		
					SET NEW.RUANG_KAMAR_TIDUR = VRUANGKAMARTIDUR;
					UPDATE pendaftaran.reservasi SET STATUS = 2 WHERE NOMOR = VRESERVASI;
				END IF;
			END IF;
		END IF;
	END IF;
END//
DELIMITER ;
