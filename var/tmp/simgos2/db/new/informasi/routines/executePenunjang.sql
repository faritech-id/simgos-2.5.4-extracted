-- membuang struktur untuk procedure informasi.executePenunjang
DROP PROCEDURE IF EXISTS `executePenunjang`;
DELIMITER //
CREATE DEFINER=`root`@`localhost` PROCEDURE `executePenunjang`(IN `PTGL_AWAL` DATE, IN `PTGL_AKHIR` DATE)
BEGIN
	DECLARE VTGL_AWAL DATETIME;
	DECLARE VTGL_AKHIR DATETIME;
		
	SET VTGL_AWAL = CONCAT(PTGL_AWAL, ' 00:00:01');
	SET VTGL_AKHIR = CONCAT(PTGL_AKHIR, ' 23:59:59');
	
	BEGIN		
		DECLARE VTANGGAL DATE;
		DECLARE VID VARCHAR(50);
		DECLARE VDESKRIPSI VARCHAR(200);
		DECLARE VIDINSTALASI VARCHAR(5);
		DECLARE VINSTALASI VARCHAR(200);
		DECLARE VIDUNIT VARCHAR(7);
		DECLARE VUNIT VARCHAR(200);
		DECLARE VIDSUBUNIT VARCHAR(10);
		DECLARE VSUBUNIT VARCHAR(200);
		DECLARE VCARABAYAR VARCHAR(200);
		DECLARE VIDCARABAYAR VARCHAR(1);
		DECLARE VVALUE BIGINT;
		DECLARE VLASTUPDATED DATETIME;
		
		DECLARE EXEC_NOT_FOUND TINYINT DEFAULT FALSE;		
		DECLARE CR_EXEC CURSOR FOR SELECT DATE(tk.MASUK) TANGGAL
							         , jk.ID ID
										, jk.DESKRIPSI DESKRIPSI
										, (SELECT i.ID FROM master.ruangan i WHERE su.ID LIKE CONCAT(i.ID,'%')  AND i.JENIS=3 LIMIT 1) IDINSTALASI
										, (SELECT i.DESKRIPSI FROM master.ruangan i WHERE su.ID LIKE CONCAT(i.ID,'%')  AND i.JENIS=3 LIMIT 1) INSTALASI
										, (SELECT u.ID FROM master.ruangan u WHERE su.ID LIKE CONCAT(u.ID,'%')  AND u.JENIS=4 LIMIT 1) IDUNIT
										, (SELECT u.DESKRIPSI FROM master.ruangan u WHERE su.ID LIKE CONCAT(u.ID,'%')  AND u.JENIS=4 LIMIT 1) UNIT
										, su.ID IDSUBUNIT
										, su.DESKRIPSI SUBUNIT
										, IF(pj.JENIS=2 AND bp.nmJenisPeserta='PBI (APBD)','PBI APBD' 
										, IF(pj.JENIS=2 AND bp.nmJenisPeserta='PBI (APBN)','PBI APBN'
										, IF(pj.JENIS=2 AND bp.nmJenisPeserta='PEKERJA SEKTOR INFORMAL INDIVIDU','NON PBI(MANDIRI)'
										, IF(pj.JENIS=2 AND (bp.nmJenisPeserta IS NULL OR bp.nmJenisPeserta NOT IN ('PBI (APBD)' ,'PBI (APBN)','PEKERJA SEKTOR INFORMAL INDIVIDU')),'NON PBI (NON MANDIRI)','NON JKN')))) CARABAYAR
										, IF(pj.JENIS=2 AND bp.nmJenisPeserta='PBI (APBD)',1
										, IF(pj.JENIS=2 AND bp.nmJenisPeserta='PBI (APBN)',2
										, IF(pj.JENIS=2 AND bp.nmJenisPeserta='PEKERJA SEKTOR INFORMAL INDIVIDU',3
										, IF(pj.JENIS=2 AND (bp.nmJenisPeserta IS NULL OR bp.nmJenisPeserta NOT IN ('PBI (APBD)' ,'PBI (APBN)','PEKERJA SEKTOR INFORMAL INDIVIDU')),4,5)))) IDCARABAYAR
										, COUNT(tk.NOMOR) VALUE
										, MAX(tk.MASUK) LASTUPDATED
									FROM pendaftaran.pendaftaran pd
										  LEFT JOIN pendaftaran.penjamin pj ON pd.NOMOR=pj.NOPEN
										  LEFT JOIN master.kartu_asuransi_pasien kap ON pd.NORM=kap.NORM AND pj.JENIS=kap.JENIS AND pj.JENIS=2
										  LEFT JOIN bpjs.peserta bp ON pd.NORM=bp.norm AND pj.JENIS=2 AND kap.NOMOR=bp.noKartu
										, pendaftaran.kunjungan tk
										, master.ruangan su
										  LEFT JOIN master.referensi jk ON su.JENIS_KUNJUNGAN=jk.ID AND jk.JENIS=15
			  				 	WHERE pd.NOMOR=tk.NOPEN AND pd.STATUS IN (1,2) AND tk.STATUS IN (1,2)
										AND tk.RUANGAN=su.ID AND su.JENIS=5 AND su.JENIS_KUNJUNGAN NOT IN (0,1,2,3)
										AND tk.MASUK BETWEEN VTGL_AWAL AND VTGL_AKHIR
								GROUP BY DATE(tk.MASUK),tk.RUANGAN, pj.JENIS
								ORDER BY DATE(tk.MASUK),IDINSTALASI, IDUNIT, IDSUBUNIT, IDCARABAYAR;
			
		DECLARE CONTINUE HANDLER FOR NOT FOUND SET EXEC_NOT_FOUND = TRUE;
						
		OPEN CR_EXEC;
						
		EXIT_EXEC: LOOP
			FETCH CR_EXEC INTO VTANGGAL, VID, VDESKRIPSI, VIDINSTALASI, VINSTALASI, VIDUNIT, VUNIT, VIDSUBUNIT, VSUBUNIT, VCARABAYAR, VIDCARABAYAR, VVALUE, VLASTUPDATED;
							
			IF EXEC_NOT_FOUND THEN
				LEAVE EXIT_EXEC;
			END IF;
			
			IF EXISTS(SELECT 1 
					FROM informasi.penunjang 
				  WHERE TANGGAL = VTANGGAL 
				    AND ID = VID AND IDINSTALASI = VIDINSTALASI AND IDUNIT = VIDUNIT AND IDSUBUNIT = VIDSUBUNIT
					 AND IDCARABAYAR = VIDCARABAYAR) THEN
				UPDATE informasi.penunjang
				   SET VALUE = VVALUE, LASTUPDATED = VLASTUPDATED
				  WHERE TANGGAL = VTANGGAL 
				    AND ID = VID AND IDINSTALASI = VIDINSTALASI AND IDUNIT = VIDUNIT AND IDSUBUNIT = VIDSUBUNIT
					 AND IDCARABAYAR = VIDCARABAYAR;
			ELSE
				INSERT INTO informasi.penunjang(TANGGAL, ID, DESKRIPSI, IDINSTALASI, INSTALASI, IDUNIT, UNIT, IDSUBUNIT, SUBUNIT, CARABAYAR, IDCARABAYAR, VALUE, LASTUPDATED)
					  VALUES (VTANGGAL, VID, VDESKRIPSI, VIDINSTALASI, VINSTALASI, VIDUNIT, VUNIT, VIDSUBUNIT, VSUBUNIT, VCARABAYAR, VIDCARABAYAR, VVALUE, VLASTUPDATED);
			END IF;
		END LOOP;
		
		CLOSE CR_EXEC;
	END;		
END//
