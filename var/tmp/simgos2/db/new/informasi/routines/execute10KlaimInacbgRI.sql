-- membuang struktur untuk procedure informasi.execute10KlaimInacbgRI
DROP PROCEDURE IF EXISTS `execute10KlaimInacbgRI`;
DELIMITER //
CREATE DEFINER=`root`@`localhost` PROCEDURE `execute10KlaimInacbgRI`(IN `PTGL_AWAL` DATE, IN `PTGL_AKHIR` DATE)
BEGIN
	DECLARE VTGL_AWAL DATETIME;
	DECLARE VTGL_AKHIR DATETIME;
		
	SET VTGL_AWAL = CONCAT(PTGL_AWAL, ' 00:00:01');
	SET VTGL_AKHIR = CONCAT(PTGL_AKHIR, ' 23:59:59');
	
	BEGIN		
		DECLARE VTANGGAL DATE;
		DECLARE VID VARCHAR(25);
		DECLARE VDESKRIPSI VARCHAR(200);
		DECLARE VVALUE BIGINT;
		DECLARE VLASTUPDATED DATETIME;
		
		DECLARE EXEC_NOT_FOUND TINYINT DEFAULT FALSE;		
		DECLARE CR_EXEC CURSOR FOR SELECT DATE(ina.TANGGAL) TANGGAL, ina.CODECBG ID
					, cd.DESKRIPSI DESKRIPSI 
					, COUNT(ina.CODECBG) VALUE
					, MAX(ina.TANGGAL) LASTUPDATED
				FROM pendaftaran.pendaftaran pd
					, pendaftaran.kunjungan tk
					, master.ruangan su
					, inacbg.hasil_grouping ina
				   , inacbg.inacbg cd
				WHERE pd.NOMOR=tk.NOPEN AND pd.STATUS IN (1,2) AND tk.STATUS IN (1,2) AND tk.REF IS NULL
						AND tk.RUANGAN=su.ID AND su.JENIS=5 AND su.JENIS_KUNJUNGAN=3 AND tk.NOPEN=ina.NOPEN AND ina.TOTALTARIF!=0
						AND ina.TANGGAL BETWEEN VTGL_AWAL AND VTGL_AKHIR AND ina.CODECBG=cd.KODE AND cd.JENIS=1
				GROUP BY DATE(ina.TANGGAL),ina.CODECBG;
			
		DECLARE CONTINUE HANDLER FOR NOT FOUND SET EXEC_NOT_FOUND = TRUE;
						
		OPEN CR_EXEC;
						
		EXIT_EXEC: LOOP
			FETCH CR_EXEC INTO VTANGGAL, VID, VDESKRIPSI, VVALUE, VLASTUPDATED;								
							
			IF EXEC_NOT_FOUND THEN
				LEAVE EXIT_EXEC;
			END IF;
			
			IF EXISTS(SELECT 1 FROM informasi.klaim_inacbg_ri WHERE TANGGAL = VTANGGAL AND ID = VID) THEN
				UPDATE informasi.klaim_inacbg_ri
				   SET VALUE = VVALUE, LASTUPDATED = VLASTUPDATED
				  WHERE TANGGAL = VTANGGAL AND ID = VID;
			ELSE
				INSERT INTO informasi.klaim_inacbg_ri(TANGGAL, ID, DESKRIPSI, VALUE, LASTUPDATED)
					  VALUES (VTANGGAL, VID, VDESKRIPSI, VVALUE, VLASTUPDATED);
			END IF;
		END LOOP;
		
		CLOSE CR_EXEC;
	END;		
END//
