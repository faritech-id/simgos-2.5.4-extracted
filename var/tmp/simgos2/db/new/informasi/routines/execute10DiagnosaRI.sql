-- membuang struktur untuk procedure informasi.execute10DiagnosaRI
DROP PROCEDURE IF EXISTS `execute10DiagnosaRI`;
DELIMITER //
CREATE DEFINER=`root`@`localhost` PROCEDURE `execute10DiagnosaRI`(IN `PTGL_AWAL` DATE, IN `PTGL_AKHIR` DATE)
BEGIN
	DECLARE VTGL_AWAL DATETIME;
	DECLARE VTGL_AKHIR DATETIME;
		
	SET VTGL_AWAL = CONCAT(PTGL_AWAL, ' 00:00:01');
	SET VTGL_AKHIR = CONCAT(PTGL_AKHIR, ' 23:59:59');
	
	BEGIN		
		DECLARE VTANGGAL DATE;
		DECLARE VID VARCHAR(25);
		DECLARE VDESKRIPSI VARCHAR(200);
		DECLARE VVALUE BIGINT;
		DECLARE VLASTUPDATED DATETIME;
		
		DECLARE EXEC_NOT_FOUND TINYINT DEFAULT FALSE;		
		DECLARE CR_EXEC CURSOR FOR SELECT DATE(tk.KELUAR) TANGGAL
		         , md.KODE ID
					, (SELECT ms.STR FROM master.mrconso ms WHERE ms.SAB='ICD10_1998' AND TTY IN ('PX', 'PT') AND ms.CODE=md.KODE LIMIT 1) DESKRIPSI
					, COUNT(md.KODE) VALUE
					, MAX(tk.MASUK) LASTUPDATED
				FROM pendaftaran.pendaftaran pd
					, pendaftaran.kunjungan tk
					, master.ruangan su
					  LEFT JOIN master.referensi jk ON su.JENIS_KUNJUNGAN=jk.ID AND jk.JENIS=15 
					, medicalrecord.diagnosa md
					, layanan.pasien_pulang pp
				WHERE pd.NOMOR=tk.NOPEN AND pd.STATUS IN (1,2) AND tk.STATUS IN (1,2) AND tk.NOMOR=pp.KUNJUNGAN AND pp.`STATUS`!=0
						AND tk.RUANGAN=su.ID AND su.JENIS=5 AND su.JENIS_KUNJUNGAN=3 AND tk.NOPEN=md.NOPEN AND md.STATUS=1
						AND tk.KELUAR BETWEEN VTGL_AWAL AND VTGL_AKHIR
				GROUP BY DATE(tk.KELUAR),ID;
			
		DECLARE CONTINUE HANDLER FOR NOT FOUND SET EXEC_NOT_FOUND = TRUE;
						
		OPEN CR_EXEC;
						
		EXIT_EXEC: LOOP
			FETCH CR_EXEC INTO VTANGGAL, VID, VDESKRIPSI, VVALUE, VLASTUPDATED;								
							
			IF EXEC_NOT_FOUND THEN
				LEAVE EXIT_EXEC;
			END IF;
			
			IF EXISTS(SELECT 1 FROM informasi.diagnosa_ri WHERE TANGGAL = VTANGGAL AND ID = VID) THEN
				UPDATE informasi.diagnosa_ri
				   SET VALUE = VVALUE, LASTUPDATED = VLASTUPDATED
				  WHERE TANGGAL = VTANGGAL AND ID = VID;
			ELSE
				INSERT INTO informasi.diagnosa_ri(TANGGAL, ID, DESKRIPSI, VALUE, LASTUPDATED)
					  VALUES (VTANGGAL, VID, VDESKRIPSI, VVALUE, VLASTUPDATED);
			END IF;
		END LOOP;
		
		CLOSE CR_EXEC;
	END;		
END//
