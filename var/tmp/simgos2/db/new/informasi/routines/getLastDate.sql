CREATE DEFINER=`root`@`127.0.0.1` FUNCTION `getLastDate`(
	`PYEAR` YEAR,
	`PMONTH` TINYINT


) RETURNS date
    DETERMINISTIC
BEGIN
	DECLARE VDATE DATE;
	
	IF DAY(NOW()) < 28 OR (YEAR(NOW()) = PYEAR AND PMONTH = MONTH(NOW())) THEN
		RETURN DATE(NOW());
	END IF;
	
	SET VDATE = DATE(CONCAT(PYEAR, '-', PMONTH, '-', 31));
	IF NOT VDATE IS NULL THEN
		RETURN VDATE;
	END IF;
	
	SET VDATE = DATE(CONCAT(PYEAR, '-', PMONTH, '-', 30));
	IF NOT VDATE IS NULL THEN
		RETURN VDATE;
	END IF;
	
	SET VDATE = DATE(CONCAT(PYEAR, '-', PMONTH, '-', 29));
	IF NOT VDATE IS NULL THEN
		RETURN VDATE;
	END IF;
	
	SET VDATE = DATE(CONCAT(PYEAR, '-', PMONTH, '-', 28));
	IF NOT VDATE IS NULL THEN
		RETURN VDATE;
	END IF;
