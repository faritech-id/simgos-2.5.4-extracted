-- membuang struktur untuk procedure informasi.listLaboratorium
DROP PROCEDURE IF EXISTS `listLaboratorium`;
DELIMITER //
CREATE DEFINER=`root`@`localhost` PROCEDURE `listLaboratorium`(IN `PTGL_AWAL` DATE, IN `PTGL_AKHIR` DATE, IN `PTYPE` TINYINT)
BEGIN
	
		
	IF PTYPE < 9 THEN
		SET @sqlText = CONCAT('
			SELECT YEAR(TANGGAL) TAHUN, MONTH(TANGGAL) IDBULAN, b.NAMA BULAN, k.ID, DESKRIPSI
					 , CONCAT(''I'', IDINSTALASI) IDINSTALASI, INSTALASI
					 , CONCAT(''I'', IDINSTALASI, ''U'', IDUNIT) IDUNIT, UNIT
					 , IDSUBUNIT, SUBUNIT
					 , IDCARABAYAR, CARABAYAR
					 , SUM(VALUE) VALUE, MAX(LASTUPDATED) LASTUPDATED
			  FROM informasi.penunjang k LEFT JOIN master.bulan b ON MONTH(TANGGAL) = b.ID
			 WHERE TANGGAL BETWEEN ''', PTGL_AWAL, ''' AND ''', PTGL_AKHIR, ''' AND k.ID = 4 ',
			 IF(PTYPE = 2, 'GROUP BY MONTH(TANGGAL), k.ID, k.IDINSTALASI, k.IDUNIT, IDSUBUNIT, IDCARABAYAR ',
			 	IF(PTYPE = 3, 'GROUP BY YEAR(TANGGAL), k.ID, k.IDINSTALASI, k.IDUNIT, IDSUBUNIT, IDCARABAYAR ',
			 		IF(PTYPE = 4, 'GROUP BY YEAR(TANGGAL), MONTH(TANGGAL), k.ID, k.IDINSTALASI, k.IDUNIT, IDSUBUNIT ',
			 			IF(PTYPE = 5, 'GROUP BY YEAR(TANGGAL), k.ID, k.IDINSTALASI, k.IDUNIT, IDSUBUNIT', 'GROUP BY k.ID, k.IDINSTALASI, k.IDUNIT, IDSUBUNIT, IDCARABAYAR ')
			 		)
			 	)
			 ),'
			 ORDER BY TAHUN, IDBULAN, k.IDINSTALASI, k.IDUNIT, k.IDSUBUNIT, k.IDCARABAYAR');
		 
	 	
		PREPARE stmt FROM @sqlText;
		EXECUTE stmt;
		DEALLOCATE PREPARE stmt;
	ELSE
	BEGIN 
		DECLARE VTGL_AWAL_BLN DATE;
		DECLARE VTGL_AWAL_THN DATE;
		
		SET VTGL_AWAL_BLN = DATE_FORMAT(PTGL_AWAL, '%Y-%m-01');
		SET VTGL_AWAL_THN = DATE_FORMAT(PTGL_AWAL, '%Y-01-01');
		
		SELECT * FROM (
			SELECT 1 ID, SUM(VALUE) VALUE, MAX(LASTUPDATED) LASTUPDATED
			  FROM informasi.penunjang
			 WHERE TANGGAL BETWEEN PTGL_AWAL AND PTGL_AKHIR AND ID = 1
			UNION 
			SELECT 2 ID, SUM(VALUE) VALUE, MAX(LASTUPDATED) LASTUPDATED
			  FROM informasi.penunjang
			 WHERE TANGGAL BETWEEN VTGL_AWAL_BLN AND PTGL_AKHIR AND ID = 1
			UNION 
			SELECT 3 ID, SUM(VALUE) VALUE, MAX(LASTUPDATED) LASTUPDATED
			  FROM informasi.penunjang
			WHERE TANGGAL BETWEEN VTGL_AWAL_THN AND PTGL_AKHIR AND ID = 1
		) A;
	END;
	END IF;
END//
DELIMITER ;

